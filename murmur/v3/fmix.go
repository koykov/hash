package murmur

// Finalizer (bit mixer) increases entropy to obtain both better distribution and fewer collisions.
// Check https://zimbry.blogspot.com/2011/09/better-bit-mixing-improving-on.html for details.

func Fmix32(h uint32) uint32 {
	h ^= h >> 16
	h *= 0x85ebca6b
	h ^= h >> 13
	h *= 0xc2b2ae35
	h ^= h >> 16
	return h
}

func Fmix64(h uint64) uint64 {
	h ^= h >> 33
	h *= 0xff51afd7ed558ccd
	h ^= h >> 33
	h *= 0xc4ceb9fe1a85ec53
	h ^= h >> 33
	return h
}
